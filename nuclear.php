Pamačius blyksnį (lyg žiūrint tiesiai į saulę): 

1. Neieškoti šviesos šaltinio, nes turite iki 10 sekundžių tokiems veiksmams:
  - Atsigulti veidu į žemę ir nykščiais užkimšti ausis, delnais akis, bei nosį.
  - Kvepuoti per sukąstus dantis
  - Tikėtis, kad pastato gabalas neužkris ant jūsų.
Stovint sporgimo banga gali sužaloti plaučius, ausų būgnelius, bei kitus vidinius organus.

Išgyvenus sprogimo bangą:

2. Niekur nekeliauti ir nevažiuoti. Rasti artimiausią rūsį ar kitą storų cementinių,
  plieninių ar švino sienų priedangą ir joje būti 48 valandas.
  Artimųjų nerasite, jei neišgyvensite pakilusio radiacijos lygio. 
